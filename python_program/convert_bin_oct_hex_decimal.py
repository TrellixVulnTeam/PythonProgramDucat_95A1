# 50. Python Convert Decimal to Octal

n = int(input("print decimal value : "))
o = oct(n)
print("decimal to octal no: ",o)

'''
output ===

print decimal value : 5
decimal to octal no:  0o5


'''


# 51. Python Decimal to Hexadecimal

n = int(input("print decimal value : "))
h = hex(n)
print("print decimal to hexadecimal value : ",h)

'''
output ===

print decimal value : 9
print decimal to hexadecimal value :  0x9

'''


# 52. Python Convert Decimal to binary.

n = int(input("print decimal value : "))
b = bin(n)
print("decimal to binary no: ",b)

'''
output ===

print decimal value : 91
decimal to binary no:  0b1011011

'''





















